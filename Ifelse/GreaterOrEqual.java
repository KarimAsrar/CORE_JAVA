 package Ifelse;
public class GreaterOrEqual {
public static void main(String[] args) {
	int n1 = 6;
	int n2 = 2;
	
	if (n1==n2)
	{
	System.out.println(n1+ " is equal to " +n2);	
	}
	if(n1>n2)
	{
		System.out.println(n1+ " is greater than "+n2);
	}
	else
	{
		System.out.println(n1+" is lesser than "+n2);
	}
	
	
}
}
