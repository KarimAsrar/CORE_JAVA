package Inputfromuser;

public class Practice1 {
	public static void main(String[] args) {
		
		//first it will check the condition then it will print the vale
		int i = 12;
		while(i<11) {
			System.out.println("Apna Bettiah");
			i++;
		 }
		//in do while-first it will print the value at least one time then it will check the condition
		//this is the onle difference btw while loop and do while loop
		 do {
			System.out.println("Apna Bettiah");
			i++;
		   }while(i<11);   //in do while we terminate in while condition by this(;)
	   }		
   }


